    private void btnRegistrar3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar3ActionPerformed
        // TODO add your handling code here:
        if (!txtCedulaU.getText().isBlank() && cmbParametro.getSelectedIndex() != -1){                 
            try {
                Validaciones.validarCedula(txtCedulaU.getText()); 
                if(!conexion.verificarEstaRegistrado(txtCedulaU.getText())){
                    throw new ErrorValidaciones("No existe un médico registrado con este número de cédula.");            
                }
                parametro = conexion.consultarParametroPorId(dictionary.get(cmbParametro.getSelectedItem().toString()), txtCedulaU.getText());
                panelUpdate.setVisible(true);
                limpiarUpdate();
                lblActual.setText(cmbParametro.getSelectedItem().toString()+" "+lblActual.getText());
                lblNuevo.setText(cmbParametro.getSelectedItem().toString()+" "+lblNuevo.getText());
                txtActual.setText(parametro);     
            } catch (SQLException |ErrorValidaciones ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", HEIGHT);
            }                  
        }else{
            JOptionPane.showMessageDialog(null,"Ingrese todos los campos.", "Error Campos", HEIGHT);
        }
    }//GEN-LAST:event_btnRegistrar3ActionPerformed

