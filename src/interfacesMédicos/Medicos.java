    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {
            // TODO add your handling code here:
            if (txtNuevo.getText().isBlank()){
                throw new ErrorValidaciones("Ingrese el nuevo valor");
            }
            if (cmbParametro.getSelectedItem().toString().equals("Especialidad")){
                Validaciones.validarEspecialidad(txtNuevo.getText());
            }
            if (cmbParametro.getSelectedItem().toString().equals("Correo_Electrónico")){
                Validaciones.validarEmail(txtNuevo.getText());
            }
            if (cmbParametro.getSelectedItem().toString().equals("Numero_De_Celular")){
                Validaciones.validarCelular(txtNuevo.getText());
            }
            conexion.actualizarAtributo(dictionary.get(cmbParametro.getSelectedItem().toString()), txtCedulaU.getText(), txtNuevo.getText());
            JOptionPane.showMessageDialog(null, "El "+cmbParametro.getSelectedItem().toString()+" se actualizo exitosamente.", "Actualización",JOptionPane.INFORMATION_MESSAGE);
            limpiarActualizacion();
        } catch (ErrorValidaciones | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", HEIGHT);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

