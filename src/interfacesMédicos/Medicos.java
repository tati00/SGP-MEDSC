    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        // TODO add your handling code here:
        limpiarConsulta();
        if (!txtCedulaC.getText().isBlank()){
            try {
                Validaciones.validarCedula(txtCedulaC.getText());
                if(!conexion.verificarEstaRegistrado(txtCedulaC.getText())){
                    throw new ErrorValidaciones("No existe un médico registrado con este número de cédula.");            
                }                
                medic = conexion.consultarInfoMedico(txtCedulaC.getText());
                txtNombreC.setText(medic.getNombres());
                txtApellidoC.setText(medic.getApellidos());
                txtNumeroC.setText(medic.getNumeroCelular());
                txtEspecialidadC.setText(medic.getEspecialidad());
                txtEmailC.setText(medic.getEmail());
                txtDireccionC.setText(medic.getDireccion());
                txtCedulaC.setText("");
            } catch (ErrorValidaciones | NoSuchPaddingException | NoSuchAlgorithmException | BadPaddingException | IllegalBlockSizeException | InvalidKeyException | SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Error Consulta", HEIGHT);
            } 
        }else{
            JOptionPane.showMessageDialog(null,"Ingrese el número de cédula", "Error", HEIGHT);
        }
    }//GEN-LAST:event_btnconsultarActionPerformed

